export const parseOMA = (omaContent: string): THREE.Shape | null => {
  try {
    const lines = omaContent.split('\n');
    let radii: number[] = [];

    // 1. Extraction des rayons (R=...)
    lines.forEach(line => {
      if (line.startsWith('R=')) {
        const values = line.substring(2).split(';').map(v => parseInt(v.trim(), 10));
        radii = [...radii, ...values];
      }
    });

    radii = radii.filter(r => !isNaN(r));

    if (radii.length === 0) return null;

    // 2. Conversion Polaire -> Cartésien
    const shape = new THREE.Shape();
    const totalPoints = radii.length;
    
    // Facteur d'échelle ajusté (0.0008)
    const scale = 0.0008; 

    for (let i = 0; i < totalPoints; i++) {
      const radius = radii[i] * scale;
      
      // L'angle commence à 0°
      const angle = (i / totalPoints) * Math.PI * 2;

      const rawX = Math.cos(angle) * radius;
      const rawY = Math.sin(angle) * radius;
      
      // ✅ FIX ROTATION OMA : Permute X et Y (rotation 90 degrés) et inverse Y (miroir)
      // Ceci est nécessaire pour que l'axe du nez/tempe soit horizontal.
      const x = rawY; 
      const y = -rawX; 

      if (i === 0) {
        shape.moveTo(x, y);
      } else {
        shape.lineTo(x, y);
      }
    }

    shape.closePath();
    return shape;

  } catch (e) {
    console.error("Erreur parsing OMA", e);
    return null;
  }
};

