  try {
    const lines = omaContent.split('\n');
    let radii: number[] = [];

    // Extraction des rayons (R=...)
    lines.forEach(line => {
      if (line.startsWith('R=')) {
        const values = line.substring(2).split(';').map(v => parseInt(v.trim(), 10));
        radii = [...radii, ...values];
      }
    });

    // Nettoyage
    radii = radii.filter(r => !isNaN(r));

    if (radii.length === 0) return null;

    // Conversion Polaire -> Cartésien
    const shape = new THREE.Shape();
    const totalPoints = radii.length;
    
    // Facteur d'échelle (OMA en 1/100mm -> ThreeJS)
    // Ajusté pour que ça rentre bien dans la vue (0.0008 semble bon)
    const scale = 0.0008; 

    for (let i = 0; i < totalPoints; i++) {
      const radius = radii[i] * scale;
      
      // OMA commence souvent côté temporal (0°)
      const angle = (i / totalPoints) * Math.PI * 2;

      // Inversion X pour orienter correctement (Oeil Droit)
      const x = -(Math.cos(angle) * radius); 
      const y = Math.sin(angle) * radius;

      if (i === 0) {
        shape.moveTo(x, y);
      } else {
        shape.lineTo(x, y);
      }
    }

    shape.closePath();
    return shape;

  } catch (e) {
    console.error("Erreur parsing OMA", e);
    return null;
  }
